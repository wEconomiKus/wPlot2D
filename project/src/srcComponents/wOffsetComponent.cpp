#include "wOffsetComponent.hpp"

namespace wEngine
{

/*
+---------------------------------------------------------------------------------------------------------------------------------------------------+
-> Constructors and destructors.
+---------------------------------------------------------------------------------------------------------------------------------------------------+
*/

OffsetComponent::OffsetComponent( sf::Vector2f offset )
:	mOffset{ offset }
{

}

/*
+---------------------------------------------------------------------------------------------------------------------------------------------------+
-> Public methods: accessors and mutators.
+---------------------------------------------------------------------------------------------------------------------------------------------------+
*/

sf::Vector2f OffsetComponent::getOffset( ) const
{
	return mOffset;
}

void OffsetComponent::setOffset( sf::Vector2f offset )
{
	mOffset = offset;
}

/*
+---------------------------------------------------------------------------------------------------------------------------------------------------+
-> Public methods.
+---------------------------------------------------------------------------------------------------------------------------------------------------+
*/

void OffsetComponent::addOffset( sf::Vector2f delta )
{
	mOffset += delta;
}

/*
+---------------------------------------------------------------------------------------------------------------------+
-> MÃ©thodes public : debug.
+---------------------------------------------------------------------------------------------------------------------+
*/

void OffsetComponent::debugPrint( ) const
{
	std::cout << "Offset: (" << getOffset( ).x << ", " << getOffset( ).y << ")\n";
}

}//End of namespace wEngine