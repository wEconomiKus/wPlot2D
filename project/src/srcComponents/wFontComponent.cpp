/**
 * @file wFontComponent.cpp
 * @brief Implementation of the FontComponent class.
 */

#include "wFontComponent.hpp"

namespace wEngine
{

/*
+---------------------------------------------------------------------------------------------------------------------------------------------------+
-> Constructors and destructors.
+---------------------------------------------------------------------------------------------------------------------------------------------------+
*/

FontComponent::FontComponent( const sf::Font& font )
:	mFont{ &font }
{

}

/*
+---------------------------------------------------------------------------------------------------------------------------------------------------+
-> Public methods: accessors and mutators.
+---------------------------------------------------------------------------------------------------------------------------------------------------+
*/

const sf::Font& FontComponent::getFont( ) const
{
	return *mFont;
}

void FontComponent::setFont( const sf::Font& font )
{
	mFont = &font;
}

/*
+---------------------------------------------------------------------------------------------------------------------------------------------------+
-> Public method: debug.
+---------------------------------------------------------------------------------------------------------------------------------------------------+
*/

void FontComponent::debugPrint( ) const
{
	std::cout << "FontComponent: font address = " << mFont << "\n";
}

}//End of namespace wEngine